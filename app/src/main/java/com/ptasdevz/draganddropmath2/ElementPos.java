package com.ptasdevz.draganddropmath2;

public class ElementPos {
    public float X;
    public float Y;
    public int action;
    public int ptrIdx;

    @Override
    public String toString() {
        return "PtrPos{" +
                "X=" + X +
                ", Y=" + Y +
                ", action=" + action +
                ", ptrIdx=" + ptrIdx +
                '}';
    }
}
